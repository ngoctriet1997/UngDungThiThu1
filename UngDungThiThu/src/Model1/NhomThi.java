package Model1;

public class NhomThi {
	int ma;
	String ten;
	
	public NhomThi(int ma, String tenNhomThi) {
		super();
		this.ma = ma;
		this.ten = tenNhomThi;
	}
	public int getMa() {
		return ma;
	}
	public void setMa(int ma) {
		this.ma = ma;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String tenNhomThi) {
		this.ten = tenNhomThi;
	}
	
}
